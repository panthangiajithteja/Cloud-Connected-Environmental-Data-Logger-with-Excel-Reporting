/*===============================================================
File: uart_interrupt.c
Purpose: UART0 Interrupt Handling for LPC21xx ARM7 MCU.

Features:
- UART Receive Interrupt Service Routine
- UART Data Buffer Storage
- UART Interrupt Initialization
===============================================================*/

#include <LPC21xx.h>
#include "uart.h"

//------------------------------------------------------------
// Global UART Buffer Variables
//------------------------------------------------------------
char buff[300], store, ch;

int i;

//------------------------------------------------------------
// Function: uart_rx
// Purpose : UART0 Interrupt Service Routine
//------------------------------------------------------------
void uart_rx(void) __irq
{
	//--------------------------------------------------------
	// Check UART Receive Interrupt
	//--------------------------------------------------------
	if(U0IIR & 0X04)
	{
		//----------------------------------------------------
		// Read Received Character
		//----------------------------------------------------
		ch = U0RBR;

		//----------------------------------------------------
		// Store Character into Buffer
		//----------------------------------------------------
		if(i < 299)
		{
			buff[i++] = ch;
		}
	}
	else
	{
		//----------------------------------------------------
		// Store Other UART Interrupt Status
		//----------------------------------------------------
		store = U0IIR;
	}

	//--------------------------------------------------------
	// End of Interrupt
	//--------------------------------------------------------
	VICVectAddr = 0;
}

//------------------------------------------------------------
// Function: init_inter
// Purpose : Initialize UART0 Interrupt
//------------------------------------------------------------
void init_inter()
{
	//--------------------------------------------------------
	// Configure IRQ Mode
	//--------------------------------------------------------
	VICIntSelect = 0x00000000;

	//--------------------------------------------------------
	// Set UART0 ISR Address
	//--------------------------------------------------------
	VICVectAddr0 = (unsigned)uart_rx;

	//--------------------------------------------------------
	// Configure UART0 Interrupt Channel
	//--------------------------------------------------------
	VICVectCntl0 = 0x20 | 6;

	//--------------------------------------------------------
	// Enable UART0 Interrupt
	//--------------------------------------------------------
	VICIntEnable = 1 << 6;

	//--------------------------------------------------------
	// Enable UART Receive Interrupt
	//--------------------------------------------------------
	U0IER = 0x03;
}
