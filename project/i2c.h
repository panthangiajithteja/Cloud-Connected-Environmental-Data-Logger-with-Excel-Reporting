/*===============================================================
File: i2c.h
Purpose: Header file for I2C interfacing with LPC21xx
ARM7 MCU.

Contains:
- I2C Pin Definitions
- I2C Control Bit Definitions
- I2C Clock Configuration
- Function Prototypes
===============================================================*/

#include <LPC21xx.h>
#include "clock.h"

//------------------------------------------------------------
// I2C Pin Definitions
//------------------------------------------------------------
#define SCL 2
#define SDA 3

//------------------------------------------------------------
// I2C Control Register Bit Definitions
//------------------------------------------------------------
#define I2EN 6
#define STA  5
#define STO  4
#define SI   3
#define AA   2

//------------------------------------------------------------
// I2C Control Clear Bits
//------------------------------------------------------------
#define I2ENC
#define STAC 5
#define SIC  3
#define AAC  2

//------------------------------------------------------------
// I2C Clock Configuration
//------------------------------------------------------------
#define S_CLK 100000

//------------------------------------------------------------
// I2C Delay Value Calculation
//------------------------------------------------------------
#define D_VAL ((PCLK / S_CLK) / 2)

//------------------------------------------------------------
// Function Prototypes
//------------------------------------------------------------

//------------------------------------------------------------
// Function: init_i2c
// Purpose : Initialize I2C Module
//------------------------------------------------------------
void init_i2c(void);

//------------------------------------------------------------
// Function: start
// Purpose : Generate START Condition
//------------------------------------------------------------
void start(void);

//------------------------------------------------------------
// Function: stop
// Purpose : Generate STOP Condition
//------------------------------------------------------------
void stop(void);

//------------------------------------------------------------
// Function: write
// Purpose : Write Data Byte through I2C
//------------------------------------------------------------
void write(char ch);

//------------------------------------------------------------
// Function: rep_start
// Purpose : Generate Repeated START Condition
//------------------------------------------------------------
void rep_start(void);

//------------------------------------------------------------
// Function: mack
// Purpose : Receive Data with ACK
//------------------------------------------------------------
char mack(void);

//------------------------------------------------------------
// Function: nack
// Purpose : Receive Data with NACK
//------------------------------------------------------------
char nack(void);
